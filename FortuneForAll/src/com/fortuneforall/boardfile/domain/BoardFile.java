package com.fortuneforall.boardfile.domain;

public class BoardFile {
	private int file_no;
	private int board_no;
	private int file_size;
	private String file_org_name;
	private String file_system_name;
	private String file_path;
	
	
	public int getFile_no() {
		return file_no;
	}
	public void setFile_no(int file_no) {
		this.file_no = file_no;
	}
	public int getBoard_no() {
		return board_no;
	}
	public void setBoard_no(int board_no) {
		this.board_no = board_no;
	}
	public int getFile_size() {
		return file_size;
	}
	public void setFile_size(int file_size) {
		this.file_size = file_size;
	}
	public String getFile_org_name() {
		return file_org_name;
	}
	public void setFile_org_name(String file_org_name) {
		this.file_org_name = file_org_name;
	}
	public String getFile_system_name() {
		return file_system_name;
	}
	public void setFile_system_name(String file_system_name) {
		this.file_system_name = file_system_name;
	}
	public String getFile_path() {
		return file_path;
	}
	public void setFile_path(String file_path) {
		this.file_path = file_path;
	}
	
	
	
	  
}