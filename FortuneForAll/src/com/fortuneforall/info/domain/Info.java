package com.fortuneforall.info.domain;

public class Info {
	private String gender;
	private String birth;
	private int solarCal;
	
	
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public int getSolarCal() {
		return solarCal;
	}
	public void setSolarCal(int solarCal) {
		this.solarCal = solarCal;
	}
	
	
	
	
	
	
}
